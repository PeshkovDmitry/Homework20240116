package my.Model;

/**
 * Класс, реализующий вычитатель
 */

public class Subtractor {

    /**
     * Метод для определения разности двух чисел
     * @param a Уменьшаемое
     * @param b Вычитаемое
     * @return Разность
     */

    public float sub(float a, float b) {
        return a - b;
    }

}