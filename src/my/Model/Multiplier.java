package my.Model;

/**
 * Класс, реализующий умножитель
 */

public class Multiplier {

    /**
     * Метод для умножения двух чисел
     * @param a Первый множитель 
     * @param b Второй множитель
     * @return Произведение
     */

    public float mul(float a, float b) {
        return a * b;
    }

}